def get_text(key, *args):
    """获取翻译文本"""
    lang = st.session_state.get('language', 'zh')
    text = translations.get(lang, translations['zh']).get(key, key)
    if args:
        return text.format(*args)
    return text

def set_language(lang):
    """设置语言"""
    st.session_state.language = lang